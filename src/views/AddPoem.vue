<template>
  <div class="add-poem">
    <h1>I heard you'd like to add another one</h1>
    <v-form ref="addForm" lazy-validation>
      <v-text-field v-model="newPoem.title" label="Title" :rules="rules" />
      <v-checkbox
        v-model="noTitle"
        label="No title here"
        @change="updateTitle('newPoem')"
      />
      <v-text-field
        v-model="newPoem.author.name"
        @blur="defineID"
        label="Author"
        :rules="rules"
      />
      <v-autocomplete
        :items="langs"
        label="Language"
        v-model="newPoem.lang"
        dense
        hide-selected
      />
      <v-autocomplete
        :items="completionStates"
        label="Status"
        v-model="status"
        dense
        hide-selected
      />
      <!-- but if i want to add another one? -->

      <wysiwyg v-model="newPoem.text" />
      <v-btn text @click="submitPoem">Add poem</v-btn>
      <v-btn text @click="clearForm">Clear</v-btn>
      <v-btn @click="defineID">TEST</v-btn>
    </v-form>
    <!--  preview?  -->
  </div>
</template>

<script>
import { mapGetters } from 'vuex';
import { uxMixin, textMixin } from '../utils';
import { generateID } from '../utils/helpers';

export default {
  name: 'AddPoem',

  mixins: [uxMixin, textMixin],

  data() {
    return {
      newPoem: {
        author: {
          name: '',
          id: ''
        },
        title: '',
        text: '',
        completed: false,
        in_progress: false,
        lang: 'English'
      },
      status: 'pending',
      completionStates: ['current', 'pending', 'completed'],
      langs: ['English', 'French', 'Spanish'],
      snackbarMessage: 'You just added a new poem',
      rules: [v => !!v || "This field shouldn't be empty"]
    };
  },

  created() {
    this.$store.dispatch('poems/getPoemsData');
  },

  computed: {
    ...mapGetters('poems', ['auhtorID'])
  },

  methods: {
    defineID() {
      if (this.auhtorID(this.newPoem.author.name) !== null) {
        this.newPoem.author.id = this.auhtorID(this.newPoem.author.name);
      } else this.newPoem.author.id = generateID();
    },

    preparePoem() {
      if (!this.newPoem.title) this.newPoem.title = '***';
      if (this.status === 'current') {
        this.newPoem.in_progress = true;
      } else if (this.status === 'completed') {
        this.newPoem.completed = true;
      }
    },

    submitPoem() {
      this.preparePoem();
      if (this.$refs.addForm.validate() && this.newPoem.text) {
        this.$store.dispatch('poems/addPoem', this.newPoem);
        this.showMessage(this.snackbarMessage);
        this.clearForm();
      }
    },

    clearForm() {
      this.newPoem = {
        author: {
          name: '',
          id: ''
        },
        title: '',
        text: '',
        completed: false,
        in_progress: false,
        lang: 'English'
      };

      this.status = 'pending';
    }
  }
};
</script>

<style scoped>
@import '~vue-wysiwyg/dist/vueWysiwyg.css';
</style>
